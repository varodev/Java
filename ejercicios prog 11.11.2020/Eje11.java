public class Eje11{
	public static void main(String args[]){
		
		
		
		char A = 'c';
        char B;
			
			System.out.println(A++);//c
			System.out.println(A++);//d
			System.out.println(++A);//f
			B = --A;
			System.out.println(++A);//f
			A++;
			--B;
			System.out.println(B++);//d
			System.out.println(++B);//f
			System.out.println(++A);//h
			System.out.println(B--);//f
			System.out.println(A);//h
			System.out.println(B);//e
		
		
		
		
		
		
	}
	
	
	
}