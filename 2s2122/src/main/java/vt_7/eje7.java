    private void btnIpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIpActionPerformed
        String url = txtFldUrl.getText();
        try {
            InetAddress dirInternet = InetAddress.getByName(url);
            String ip = dirInternet.getHostAddress();
            String mensaje = "La direcci√≥n IP: "+ip+"\nse correponde con la " + "url: " + url + ".";
            JOptionPane.showMessageDialog(null, mensaje, "Mensaje de " + "informacion", JOptionPane.INFORMATION_MESSAGE);
        } catch (UnknownHostException ex) {
            JOptionPane.showMessageDialog(this, "url no valida\nPrueba con otra ip", "Mensaje de error" , JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIpActionPerformed

