package ev1.ejercicios;

public class EjercicioOperacionesANivelDeBit {

	public static void main(String[] args) {

		int a = -100; // binario: 11111111 11111111 11111111 10011100
//		int a = 0b11111111_11111111_11111111_10011100;
		
		int b = 5; // binario: 00000000 00000000 00000000 00000101
//		int b = 0b00000000_00000000_00000000_00000101;
		
		// a & b
		// 11111111 11111111 11111111 10011100
		// 00000000 00000000 00000000 00000101
		// 00000000 00000000 00000000 00000100 = 4
		System.out.println("a & b = " + (a & b));
		
		// a | b
		// 11111111 11111111 11111111 10011100
		// 00000000 00000000 00000000 00000101
		// 11111111 11111111 11111111 10011101 = -99
		System.out.println("a | b = " + (a | b));
		
		// a ^ b
		// 11111111 11111111 11111111 10011100
		// 00000000 00000000 00000000 00000101
		// 11111111 11111111 11111111 10011001 = -103
		System.out.println("a ^ b = " + (a ^ b));
		
		// ~a
		// 11111111 11111111 11111111 10011100
		// 00000000 00000000 00000000 01100011 = 99
		System.out.println("~a = " + (~a));
		
		// a << b
		// 11111111 11111111 11111111 10011100
		// 11111111 11111111 11110011 10000000 = -3.200
		System.out.println("a << b = " + (a << b));
		
		// a >> b
		// 11111111 11111111 11111111 10011100
		// 11111111 11111111 11111111 11111100 = -4
		System.out.println("a >> b = " + (a >> b));
		
		// a >>> b
		// 11111111 11111111 11111111 10011100
		// 00000111 11111111 11111111 11111100 = 134.217.724
		System.out.println("a >>> b = " + (a >>> b));
	}
}
